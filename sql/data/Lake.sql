	Name VARCHAR2(50) CONSTRAINT LakeKey PRIMARY KEY,
	River VARCHAR2(50),
	Area NUMBER CONSTRAINT LakeAr CHECK (Area >= 0),
	Elevation NUMBER,
	`Depth` NUMBER CONSTRAINT LakeDpth CHECK (Depth >= 0),
	Height NUMBER CONSTRAINT DamHeight CHECK (Height > 0),
