	Latitude NUMBER CONSTRAINT LakeCoord_Latitude CHECK (Latitude >= -90 AND Latitude <= 90),
	Longitude NUMBER CONSTRAINT LakeCoord_Longitude CHECK (Longitude > -180 AND Longitude <= 180)
)
;

