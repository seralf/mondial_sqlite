CREATE TABLE IF NOT EXISTS River(
	Name VARCHAR2(50) CONSTRAINT RiverKey PRIMARY KEY,
	River VARCHAR2(50),
	Lake VARCHAR2(50),
	Sea VARCHAR2(50),
	`Length` NUMBER CONSTRAINT RiverLength CHECK (Length >= 0),
	Area NUMBER CONSTRAINT RiverArea CHECK (Area >= 0),
	Source_Latitude NUMBER CONSTRAINT SourceCoord_Latitude CHECK (Source_Latitude >= -90 AND Source_Latitude <= 90),
	Source_Longitude NUMBER CONSTRAINT SourceCoord_Longitude CHECK (Source_Longitude > -180 AND Source_Longitude <= 180),
	Mountains VARCHAR2(50),
	SourceElevation NUMBER,
	Estuary_Latitude NUMBER CONSTRAINT EstuaryCoord_Latitude CHECK (Estuary_Latitude >= -90 AND Estuary_Latitude <= 90),
	Estuary_Longitude NUMBER CONSTRAINT EstuaryCoord_Longitude CHECK (Estuary_Longitude > -180 AND Estuary_Longitude <= 180),    
	EstuaryElevation NUMBER,
	CONSTRAINT RivFlowsInto CHECK ((River IS NULL AND Lake IS NULL) OR (River IS NULL AND Sea IS NULL) OR (Lake IS NULL AND Sea is NULL))
)
;

