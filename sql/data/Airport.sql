;

CREATE TABLE IF NOT EXISTS Airport(
	IATACode VARCHAR(3) PRIMARY KEY,
	Name VARCHAR2(100),
	Country VARCHAR2(4),
	City VARCHAR2(50),
	Province VARCHAR2(50),
	Island VARCHAR2(50),
	Latitude NUMBER CONSTRAINT DesertCoord_Latitude CHECK (Latitude >= -90 AND Latitude <= 90),
	Longitude NUMBER CONSTRAINT DesertCoord_Longitude CHECK (Longitude > -180 AND Longitude <= 180),
	Elevation NUMBER,
	gmtOffset NUMBER, 
	CONSTRAINT FK_AirportCity FOREIGN KEY(City) REFERENCES City(Name)
)
;

